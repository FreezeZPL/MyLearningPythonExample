def cheeses_and_crackers(chesses_cout,crackers_cout):
    print("You have %d cheeses" %chesses_cout)
    print("You have %d boxes of crackers" %crackers_cout)

cheeses_and_crackers(10,20)

a = 10
b = 20
cheeses_and_crackers(a,b)

cheeses_and_crackers(a+b,b-a)

cheeses_and_crackers(a*b,b/a)

cheeses_and_crackers(a+10,b+10)

cheeses_and_crackers(a+b,10+20)

